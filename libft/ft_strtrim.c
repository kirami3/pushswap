#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	char	*p;
	int		size;

	if (!s1 || !set)
		return (NULL);
	while (*s1 && ft_strchr(set, *s1))
		s1++;
	size = ft_strlen(s1);
	while (size && ft_strchr(set, s1[size]))
		size--;
	p = ft_substr((char *)s1, 0, size + 1);
	return (p);
}
